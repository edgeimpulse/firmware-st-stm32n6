/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  RNG_ConfigTypeDef Conf;
  RNG_ConfigTypeDef Conf_read;
  uint32_t counter = 0;

  
  /* STM32N6xx HAL library initialization:
       - Systick timer is configured by default as source of time base, but user
         can eventually implement his proper time base source (a general purpose
         timer for example or other time source), keeping in mind that Time base
         duration should be kept 1ms since PPP_TIMEOUT_VALUEs are defined and
         handled in milliseconds basis.
       - Set NVIC Group Priority to 4
       - Low Level Initialization
     */
  /* USER CODE END 1 */

  /* Enable the CPU Cache */

  /* Enable I-Cache---------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache---------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration--------------------------------------------------------*/
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_RNG_Init();
  /* USER CODE BEGIN 2 */

  /* Initialize LED Error on board */
  BSP_LED_Init(LED2);

  /* Configure USER push-button in Interrupt mode */
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);
 
  /* Modify RNG peripheral parameters : compliant with German BSI AIS-31 statistical tests (T0 to T8),
    but not certified NIST-compliant */
  Conf.ClockDivider = RNG_CLKDIV_BY_128; /* No clock division */
  Conf.Config1 = 0x18;
  Conf.Config2 = 0x0;
  Conf.Config3 = 0x0;
  Conf.NistCompliance = RNG_CUSTOM_NIST;
  Conf.AutoReset = RNG_ARDIS_ENABLE;
  /* Set RNG new configuration */
  HAL_RNGEx_SetConfig(&hrng, &Conf);
  /* Lock RNG peripheral configuration */
  HAL_RNGEx_LockConfig(&hrng);

  /* Read RNG peripheral configuration and check
     that parameters are those configured */
  HAL_RNGEx_GetConfig(&hrng, &Conf_read);
  if ((Conf_read.ClockDivider != RNG_CLKDIV_BY_128) || \
      (Conf_read.Config1 != 0x18)                 || \
      (Conf_read.Config2 != 0x0)                  || \
      (Conf_read.Config3 != 0x0)                  || \
      (Conf_read.NistCompliance != RNG_CUSTOM_NIST))
  {
    Error_Handler();
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  while (1)
  {

    /* Wait for event on push button to perform following actions */
    while ((ubUserButtonClickEvent) == RESET)
    {
      __NOP();
    }
    /* Reset variable for next loop iteration */
    ubUserButtonClickEvent = RESET;

    /* Generate eigh.t 32-bit long random numbers */
    for (counter = 0; counter < 8; counter++)
    {
      if (HAL_RNG_GenerateRandomNumber(&hrng, &aRandom32bit[counter]) != HAL_OK)
      {
        /* Random number generation error */
        Error_Handler();
      }
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */
}
